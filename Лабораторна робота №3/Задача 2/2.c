#include <stdio.h>

int main() {
    int num1, num2;
    int sumDigitsNum1, sumDigitsNum2;
                //sumDigitsNum1 це Сума цифр числа(2) 
    // Зчитування першого тризначного числа
    printf("Введіть перше тризначне число: ");
    scanf("%d", &num1);

    // Перевірка, чи введене число є тризначним
    if (num1 < 100 || num1 > 999) {
        printf("Помилка: введіть ціле тризначне число.\n");
        return 1; // Повертаємо 1, щоб показати, що програма закінчилася з помилкою
    }

    // Зчитування другого тризначного числа
    printf("Введіть друге тризначне число: ");
    scanf("%d", &num2);

    // Перевірка, чи введене число є тризначним
    if (num2 < 100 || num2 > 999) {
        printf("Помилка: введіть ціле тризначне число.\n");
        return 1; // Повертаємо 1, щоб показати, що програма закінчилася з помилкою
    }

    // Обчислення суми цифр першого числа
    sumDigitsNum1 = (num1 / 100) + ((num1 / 10) % 10) + (num1 % 10);

    // Обчислення суми цифр другого числа
    sumDigitsNum2 = (num2 / 100) + ((num2 / 10) % 10) + (num2 % 10);

    // Порівняння сум цифр і виведення результату
    if (sumDigitsNum1 > sumDigitsNum2) {
        printf("\nСума цифр першого числа (%d) більша за суму цифр другого числа (%d).\n", sumDigitsNum1, sumDigitsNum2);
    } else if (sumDigitsNum2 > sumDigitsNum1) {
        printf("\nСума цифр другого числа (%d) більша за суму цифр першого числа (%d).\n", sumDigitsNum2, sumDigitsNum1);
    } else {
        printf("\nСума цифр обох чисел однакова (%d).\n", sumDigitsNum1);
    }

    return 0;
}
